export const translateText = (text: string, fromLang: string, toLang: string): string => {
const translations: Record<string, Record<string, string>> = {
  'hello': {
    spanish: 'Hola',
    french: 'Bonjour',
    german: 'Hallo',
    urdu: 'ہیلو',
    arabic: 'مرحبا'
  },
  'thank you': {
    spanish: 'Gracias',
    french: 'Merci',
    german: 'Danke',
    urdu: 'شکریہ',
    arabic: 'شكرا'
  },
  'how are you': {
    spanish: '¿Cómo estás?',
    french: 'Comment allez-vous?',
    german: 'Wie geht es dir?',
    urdu: 'آپ کیسے ہیں؟',
    arabic: 'كيف حالك؟'
  }
};


  const lowerText = text.toLowerCase();
  const translation = translations[lowerText]?.[toLang] || 
                     `[${toLang.toUpperCase()}: Translation for "${text}"]`;

  return `${text}\n↓\n${translation}\n\nPronunciation: ${getPronunciation(toLang, translation)}`;
};

// Helper functions
function getClosingForTone(tone: string): string {
  const closings = {
    professional: 'Best regards,',
    casual: 'Cheers,',
    persuasive: 'Looking forward to your response,',
    friendly: 'All the best,',
    formal: 'Respectfully,'
  };
  return closings[tone as keyof typeof closings] || 'Sincerely,';
}

function getPronunciation(lang: string, text: string): string {
  const pronunciations: Record<string, string> = {
    spanish: `Pronounced: ${text}`,
    french: `Pronounced: ${text}`,
    german: `Pronounced: ${text}`,
    urdu: 'Right-to-left script',
    arabic: 'Right-to-left script'
  };
  return pronunciations[lang] || text;
}

