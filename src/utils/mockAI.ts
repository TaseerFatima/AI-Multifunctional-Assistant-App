export const generateEmail = (options: {
  tone: string;
  purpose: string;
  keyPoints: string;
  subject: string;
}): string => {
  const tones = {
    professional: 'Dear [Recipient],\n\nI hope this message finds you well.',
    casual: 'Hey [Name],\n\nHope you\'re doing great!',
    persuasive: 'Hello [Recipient],\n\nI have an exciting opportunity to share with you.',
    friendly: 'Hi [Name],\n\nIt was great connecting with you!',
    formal: 'To Whom It May Concern,\n\nI am writing to formally address...'
  };

  const purposes = {
    'business': 'Regarding our business collaboration',
    'job-application': 'Job Application for [Position]',
    'follow-up': 'Following up on our previous conversation',
    'thank-you': 'Thank you for your assistance',
    'announcement': 'Important Announcement'
  };

  const toneText = tones[options.tone as keyof typeof tones] || tones.professional;
  const purposeText = purposes[options.purpose as keyof typeof purposes] || '';
  
  const currentDate = new Date().toLocaleDateString('en-US', {
    weekday: 'long',
    year: 'numeric',
    month: 'long',
    day: 'numeric'
  });

  return `Subject: ${options.subject || 'Important Message'}

${toneText}

${purposeText ? purposeText + ':\n\n' : ''}${options.keyPoints.split('\n').map(point => `• ${point}`).join('\n')}

${getClosingForTone(options.tone)}

[Your Name]
[Your Position]
[Your Contact Information]

---
Generated by AI Assistant on ${currentDate}`;
};

